import java.util.*;
import java.lang.*;
import java.io.*;

/* Name of the class has to be "Main" only if the class is public. */
 class Alphabet
{
	public static void main (String[] args) throws java.lang.Exception
	{
	  char c= '@';
	  if( (c>='a' && c<='z') || (c>='A' && c<='Z') )
	  System.out.print(c +"Alphabet");
	  else
	  System.out.print(c +"No");
	
		
		
	}
}
